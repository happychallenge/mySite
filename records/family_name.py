def search_person_from_joins(q):
    search_url = "http://people.joins.com/Search/"    
    params = {'q': q}
    html = requests.get(search_url, params=params).text
    soup = BeautifulSoup(html, 'html.parser')
    
    result = []
    for tag in soup.select('.result_list .img_listarea > li > dl'):
        a_tag = tag.find('a')
        url = urljoin(search_url, a_tag['href'])
        name = a_tag.text

        attrs = {'url': url, 'name': name}
        for dd_tag in tag.select('dd'):
            key = dd_tag.find('strong').text
            value = dd_tag.find('span').text
            attrs[key] = value

        result.append(attrs)
    return result
	
